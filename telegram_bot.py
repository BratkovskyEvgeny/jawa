        for i in range(0, len(ads), 5):
            batch = ads[i : i + 5]
            await self._send_ads_batch(update, batch)
            
        # –î–æ–±–∞–≤–ª—è–µ–º –æ—Ç–ª–∞–¥–æ—á–Ω—É—é –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é
