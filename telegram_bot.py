            ads = self.db.get_new_advertisements(limit=100)
            ad = next((ad for ad in ads if ad["id"] == ad_id), None)

            if not ad:
                await update.callback_query.answer("–û–±—ä—è–≤–ª–µ–Ω–∏–µ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ")
                return

